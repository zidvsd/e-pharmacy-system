    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        String name = tfName.getText();
        String email = tfEmail.getText();
        String phone = tfPhone.getText();
        String address = tfAddress.getText();
        
        if(name.isEmpty() || email.isEmpty() || phone.isEmpty() || address.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Please enter all fields", "Please try again", JOptionPane.ERROR_MESSAGE);
        }
        else{
            DefaultTableModel model = (DefaultTableModel) tablePatients.getModel();
            model.addRow(new Object[]{name, email, phone , address});
        
            tfName.setText("");
            tfEmail.setText("");
            tfPhone.setText("");
            tfAddress.setText("");
        }
    }//GEN-LAST:event_btnAddActionPerformed

