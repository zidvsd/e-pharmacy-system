    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int row = tablePatients.getSelectedRow();     
        if(row<0) {
            JOptionPane.showMessageDialog(this, "No row is selected. Please select one row", "Select Row", JOptionPane.ERROR_MESSAGE);}
        else{
            DefaultTableModel model = (DefaultTableModel) tablePatients.getModel();
                    model.removeRow(row);
            }
    }//GEN-LAST:event_btnDeleteActionPerformed

