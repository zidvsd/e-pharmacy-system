    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (txtMedicineName.getText().isEmpty() || txtMedicineStocks.getText().isEmpty() || 
        txtCompanyName.getText().isEmpty() || txtMedicineCategory.getText().isEmpty()) {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill all fields!");
        return;
    }

    if (!txtMedicineStocks.getText().matches("\\d+")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Stocks must be a whole number!");
        return;
    }

    if (!txtMedicineUnitPrice.getText().matches("\\d*\\.?\\d+")) {
        javax.swing.JOptionPane.showMessageDialog(this, "Unit Price must be a valid price!");
        return;
    }

    // 2. Find the index to UPDATE
    int rowIndex = -1;
    for (int i = 0; i < DataStore.medicineCount; i++) {
        if (DataStore.medicines[i][0].equals(this.medicineId)) {
            rowIndex = i;
            break;
        }
    }

    // 3. Overwrite the existing data (Don't increment medicineCount!)
    if (rowIndex != -1) {
        DataStore.medicines[rowIndex][1] = txtMedicineName.getText();
        DataStore.medicines[rowIndex][2] = txtMedicineStocks.getText();
        DataStore.medicines[rowIndex][3] = txtMedicineUnitPrice.getText();
        DataStore.medicines[rowIndex][5] = txtCompanyName.getText();
        DataStore.medicines[rowIndex][6] = txtMedicineCategory.getText();

        javax.swing.JOptionPane.showMessageDialog(this, "Medicine updated successfully!");
        this.dispose();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

