    private void loadMedicine(){
        int rowIndex = -1;
    
    for (int i = 0; i < DataStore.medicineCount; i++) {
        if (DataStore.medicines[i][0].equals(medicineId)) {
            rowIndex = i;
            break;
        }
    }

    if (rowIndex == -1) {
        javax.swing.JOptionPane.showMessageDialog(this, "Medicine not found!");
        this.dispose();
        return;
    }
    txtMedicineName.setText(DataStore.medicines[rowIndex][1]);
    txtMedicineStocks.setText(DataStore.medicines[rowIndex][2]);
    txtMedicineUnitPrice.setText(DataStore.medicines[rowIndex][3]);
    txtCompanyName.setText(DataStore.medicines[rowIndex][5]);
    txtMedicineCategory.setText(DataStore.medicines[rowIndex][6]);
    }
